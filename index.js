function makeInt(n){
  parseInt(n)
  return n
}

function preserveDecimal(n){
  parseFloat(n)
  return n
}

function add(a, b){
  sum = a + b
  return sum
}

function subtract(a, b){
  x = a - b
  return x
}

function multiply (a, b){
  product = a*b
  return product
}

function divide(a, b){
  v = a/b
  return v
}

function inc(n){
  n += 1
  return n
}

function dec(n) {
  n -= 1
  return n
}