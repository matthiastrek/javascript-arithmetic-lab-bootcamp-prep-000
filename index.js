function makeInt(n){
  parseInt(n)
  return n
}

function preserveDecimal(n){
  parseFloat(n)
  return n
}

function add(a, b){
  var sum = a + b
  return sum
}

function subtract(a, b){
  var x = a - b
  return x
}

function multiply (a, b){
  var product = a*b
  return product
}

function divide(a, b){
  var v = a/b
  return v
}

function inc(n){
  n += 1
  return n
}

function dec(m) {
  m -= 1
  return m
}